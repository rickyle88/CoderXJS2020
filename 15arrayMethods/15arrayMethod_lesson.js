var a = [1,2,3];

var b = [10,20];

// concat => to new array
let c = a.concat(b);

// push => return length of new array
a.push(5);

// pop ==> pop and return last element
a.pop();

// shift ==> remove and return first element
a.shift();

// unshift ==> add element at the begining of array and return length of new array 
a.unshift(-1);


